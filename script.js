function getData() {
    var day = s('input-day').value;
    var month = s('input-month').value;
    for (let i = 0; i < data.length; i++) {
        var dataDate = data[i]['Date'].split(' ');
        if (month == dataDate[0] && day == dataDate[1]) {
            s('input-data').style.display = "none";
            s('again-btn').style.display = 'block'
            s('result-display').style.display = 'flex';
            s('result-title').innerHTML = data[i].Name;
            s('result-img').src = `https://imagine.gsfc.nasa.gov/hst_bday/images/${data[i].Image}`;
            s('result-summary').innerHTML = data[i].Caption;
        }
    }
}

